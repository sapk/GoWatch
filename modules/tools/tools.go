//(sources from gogs project)
func logn(n, b float64) float64 {
	return math.Log(n) / math.Log(b)
}

//(sources from gogs project)
func humanateBytes(s uint64, base float64, sizes []string) string {
	if s < 10 {
		return fmt.Sprintf("%dB", s)
	}
	e := math.Floor(logn(float64(s), base))
	suffix := sizes[int(e)]
	val := float64(s) / math.Pow(base, math.Floor(e))
	f := "%.0f"
	if val < 10 {
		f = "%.1f"
	}

	return fmt.Sprintf(f+"%s", val, suffix)
}

// FileSize calculates the file size and generate user-friendly string. (sources from gogs project)
func FileSize(s int64) string {
	sizes := []string{"B", "KB", "MB", "GB", "TB", "PB", "EB"}
	return humanateBytes(uint64(s), 1024, sizes)
}

//TimeSinceHuman return a more human readable time duration
func TimeSinceHuman(start time.Time) string {
	//TODO
	//return time.Since(start).String()
	diff := time.Since(start)
	days := int(diff.Hours() / 24)
	hours := int(diff.Hours()) - 24*days
	minutes := int(diff.Minutes()) - (24*days+hours)*60
	seconds := int(diff.Seconds()) - ((24*days+hours)*60+minutes)*60
	switch {
	case days > 0:
		return fmt.Sprintf("%dD %dH", days, hours)
	case hours > 0:
		return fmt.Sprintf("%dH %dM", hours, minutes)
	default:
		return fmt.Sprintf("%dM %dS", minutes, seconds)
	}
}

